public class Empreendimento {
    private int id;
    private String nome;
    private int idSede;
    private int idTema;
    private int idAgrupador;
    private int idAndamento;
    private int idFaseGrupo;
    private int idProjeto;
    private int idInstituicao;
    private Float valTotalPrevisto;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdSede() {
        return idSede;
    }

    public void setIdSede(int idSede) {
        this.idSede = idSede;
    }

    public int getIdTema() {
        return idTema;
    }

    public void setIdTema(int idTema) {
        this.idTema = idTema;
    }

    public int getIdAgrupador() {
        return idAgrupador;
    }

    public void setIdAgrupador(int idAgrupador) {
        this.idAgrupador = idAgrupador;
    }

    public int getIdAndamento() {
        return idAndamento;
    }

    public void setIdAndamento(int idAndamento) {
        this.idAndamento = idAndamento;
    }

    public int getIdFaseGrupo() {
        return idFaseGrupo;
    }

    public void setIdFaseGrupo(int idFaseGrupo) {
        this.idFaseGrupo = idFaseGrupo;
    }

    public int getIdProjeto() {
        return idProjeto;
    }

    public void setIdProjeto(int idProjeto) {
        this.idProjeto = idProjeto;
    }

    public int getIdInstituicao() {
        return idInstituicao;
    }

    public void setIdInstituicao(int idInstituicao) {
        this.idInstituicao = idInstituicao;
    }

    public Float getValTotalPrevisto() {
        return valTotalPrevisto;
    }

    public void setValTotalPrevisto(Float valTotalPrevisto) {
        this.valTotalPrevisto = valTotalPrevisto;
    }

  
    
}
