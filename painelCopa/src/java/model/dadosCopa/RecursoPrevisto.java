public class RecursoPrevisto {
    private int id;
    private int idEmpreendimento;
    private float valRecurso;
    private float valContrPartida;
    private int idInstituicao;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getIdEmpreendimento() {
        return idEmpreendimento;
    }

    public void setIdEmpreendimento(int idEmpreendimento) {
        this.idEmpreendimento = idEmpreendimento;
    }

    public float getValRecurso() {
        return valRecurso;
    }

    public void setValRecurso(float valRecurso) {
        this.valRecurso = valRecurso;
    }

    public float getValContrPartida() {
        return valContrPartida;
    }

    public void setValContrPartida(float valContrPartida) {
        this.valContrPartida = valContrPartida;
    }

    public int getIdInstituicao() {
        return idInstituicao;
    }

    public void setIdInstituicao(int idInstituicao) {
        this.idInstituicao = idInstituicao;
    }

    
}
